<h4>F. e-invoice – Salient Features:</h4>
<ul>
  <li>Presently, e-invoicing is not voluntary, i.e. entities having prescribed aggregate turnover only can report invoices to IRP.</li>
<li>IRP is only a pass through validation portal. Certain key fields will be validated on IRP. So, IRN will be generated instantaneously, in sub-200 millisecond duration. The server capacity is robust enough to handle simultaneous uploads. Further, multiple IRPs will be made available to distribute the load of invoice registration.</li>
<li>Where e-invoicing is applicable, carrying e-invoice print during transportation of goods is not mandatory. As per Rule 138A(2) of CGST Rules, where e-invoicing is applicable, <i>"the Quick Reference (QR) code having an embedded Invoice Reference Number (IRN) in it, may be produced electronically, for verification by the proper officer, in lieu of the physical copy of such tax invoice."</i></li>
<li>There is no validation to the effect that the 'document date' (in the payload to IRP) has to be within a specified time window, for reporting to IRP/generation of IRN.</li>
<li>Penal provisions for not issuing invoice in accordance with GST Law/rules are provided in Section 122 of CGST/SGST Act, read with CGST/SGST Rules.</li>
<li>An invoice/IRN can't be cancelled partially. It has to be cancelled in toto. Cancellation of invoices is governed by Accounting Standards and other applicable rules/regulations.</li>
<li>An IRN/invoice reported to IRP can be cancelled within 24 hours. In case of cancellation of IRN, GSTR-1 also will be updated with such ‘cancelled’ status. However, if the connected e-way bill is active or verified by officer during transit, cancellation of IRN is <u><strong>not</strong></u> permitted.</li>
<li>Obtaining IRN by notified taxpayers is a legal requirement before issue of specified documents to recipients. Upon generation of IRN, amendments are not possible on IRP and cancellation of IRN only is permitted within a time window. So, the details of invoices actually issued during the tax period have to be reported in GSTR-1.<br>
  GSTR-1 is the final and self-assessed statutory statement of outward supplies by taxpayer, at the end of tax period. In the e-invoices auto-populated in GSTR-1 tables, taxpayers shall delete the details, wherever required, as per the documents actually cancelled during the tax period.</li>
<li>Once an IRN is cancelled, the concerned invoice number cannot be used again to generate another e-invoice/IRN.</li>
<li>On successful reporting of invoice details to IRP, the GST system will auto-populate them into GSTR-1 of the supplier and GSTR-2A of respective receivers. With source marked as 'e-invoice', IRN and IRN date will also be shown in GSTR-1 and GSTR-2A.</li>
<li>For items outside GST levy, separate invoice may be given by such businesses, e.g. <i>a hotel invoicing a B2B supply where the supply includes food and beverages (leviable to GST) and Alcoholic beverages (outside GST).</i></li>
<li>IRP doesn't store invoices and the facility to download e-invoice JSON is available only for limited number of days on the IRP. So, the taxpayers shall take care accordingly.</li>
<li>Even in the e-invoicing regime, while transporting goods, wherever the e-way bill is needed, the requirement continues to be mandatory.</li>
<li><strong>B2C Dynamic QR Code:</strong>  Notification No. 14/2020-Central Tax dated 21st March, 2020 (as amended)) mandated certain entities to include a dynamic Quick Response Code (QR Code) on their B2C invoices. This is a separate requirement and has no relevance or applicability to 'e-invoicing' under Rule 48(4).</li>
</ul>
